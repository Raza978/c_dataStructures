#ifndef SEARCHINGALGORITHMS_H
#define SEARCHINGALGORITHMS_H

/**
 * @brief Traverse the array and compare each element to the target value until a match is found or the end of the array is reached.
 * 
 */
void LinearSearch(void);

/**
 * @brief Repeatedly divide the sorted array in half until the target value is found.
 * 
 * @note Binary search requires a sorted array to work correctly.
 */
void BinarySearch(void);

#endif //SEARCHINGALGORITHMS_H